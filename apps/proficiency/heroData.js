class HeroProficiency {
  constructor(
    uid,
    name,
    alias,
    platform,
    photo,
    area,
    areaPower,
    city,
    cityPower,
    province,
    provincePower,
    guobiao,
    stamp,
    updatetime
  ) {
    this.uid = uid;
    this.name = name;
    this.alias = alias;
    this.platform = platform;
    this.photo = photo;
    this.area = area;
    this.areaPower = areaPower;
    this.city = city;
    this.cityPower = cityPower;
    this.province = province;
    this.provincePower = provincePower;
    this.guobiao = guobiao;
    this.stamp = stamp;
    this.updatetime = updatetime;
  }
}

export default HeroProficiency;
